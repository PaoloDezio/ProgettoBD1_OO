
public class Presenza extends Lezione {
	private String nomeAula;
	private Integer postiDisponibili;
	private Integer postiTotali;
	private String codiceAula;
	
	
	public String getNomeAula() {
		return nomeAula;
	}
	public void setNomeAula(String nomeAula) {
		this.nomeAula = nomeAula;
	}
	public Integer getPostiDisponibili() {
		return postiDisponibili;
	}
	public void setPostiDisponibili(Integer postiDisponibili) {
		this.postiDisponibili = postiDisponibili;
	}
	public Integer getPostiTotali() {
		return postiTotali;
	}
	public void setPostiTotali(Integer postiTotali) {
		this.postiTotali = postiTotali;
	}
	public String getCodiceAula() {
		return codiceAula;
	}
	public void setCodiceAula(String codiceAula) {
		this.codiceAula = codiceAula;
	}
	
	
	
}
